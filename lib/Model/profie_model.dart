class ProfileModel {
  final String name;
  final String address;
  final int age;
  final String bloodGroup;
  final double weight;
  final int heartRate;
  final String profileImage;

  ProfileModel({
    required this.name,
    required this.address,
    required this.age,
    required this.bloodGroup,
    required this.weight,
    required this.heartRate,
    required this.profileImage,
  });
}
